// 단원 id로 단원 정보 조회
export function getUnitById(id) {
  for (const semester of Object.keys(CURRICULUM_2022)) {
    const found = CURRICULUM_2022[semester].find(u => u.id === id);
    if (found) return { ...found, semester };
  }
  return null;
}

// id 배열 → 사람이 읽을 수 있는 요약 문자열
// 예: ["m1-1-1", ..., "m2-2-1"] → "중1-1 전체, 중1-2 전체, 중2-1 전체, 중2-2 삼각형의 성질"
export function summarizeProgress(ids) {
  if (!Array.isArray(ids) || ids.length === 0) return "진도 미설정";
  const bySemester = {};
  ids.forEach(id => {
    const unit = getUnitById(id);
    if (!unit) return;
    if (!bySemester[unit.semester]) bySemester[unit.semester] = [];
    bySemester[unit.semester].push(unit.name);
  });
  const parts = [];
  for (const sem of Object.keys(CURRICULUM_2022)) {
    if (!bySemester[sem]) continue;
    const total = CURRICULUM_2022[sem].length;
    const done = bySemester[sem].length;
    if (done === total) parts.push(`${sem} 전체`);
    else parts.push(`${sem} ${bySemester[sem].join("·")}`);
  }
  return parts.join(", ");
}

